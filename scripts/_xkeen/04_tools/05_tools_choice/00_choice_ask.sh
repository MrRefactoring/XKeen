# Единый слой диалога с пользователем
#
# Все интерактивные запросы проходят через эти функции, поэтому внешний вид
# меню меняется правкой одного файла, а не два десятка мест по всему проекту.
#
# Пункт меню задаётся строкой "ключ|подпись" или "ключ|подпись|состояние".
# Состояния:
#   dim     — пункт неактивен: выводится курсивом и не принимается как ответ
#             (там, где действие сейчас невозможно — например, нечего обновлять).
#   default — пункт по умолчанию: пустой Enter и q выбирают его. В ask_many это
#             «Пропустить» (отказ): в стрелочном режиме отдельная команда без
#             отметки, в числовом — его нельзя совмещать с другими номерами.
#   confirm — только ask_many, стрелочный режим: команда «Подтвердить», которая
#             фиксирует отмеченный набор. В числовом режиме не нужна (Enter и так
#             коммитит) и не показывается.
#
# Клавиши в стрелочном режиме: Space и Enter активируют пункт под курсором —
# в ask_one это выбор пункта, в ask_many — отметка переключателя либо выполнение
# команды (confirm/default). Выход из меню — q (к пункту default).
#
# Результат возвращается через глобальные переменные, а не через подстановку
# команд: $(...) выполняется в субшелле, из которого нельзя читать терминал.
#
#   ask_one    -> REPLY_KEY    один пункт из списка
#   ask_many   -> REPLY_KEYS   несколько пунктов через пробел
#   ask_value  -> REPLY_VALUE  произвольное значение с проверкой
#   ask_yesno  -> код возврата 0 (Да) или 1 (Нет)
#
# Состояние слоя хранится в глобальных _ask_*, поэтому вложенный вызов
# ask_* затрёт состояние внешнего. На практике это ограничение касается
# валидатора для ask_value: он вызывается изнутри цикла чтения, и если
# запросит подтверждение через ask_yesno, то затрёт _ask_input и оставит
# внешний цикл крутиться на пустом значении. Валидатор обязан быть
# чистой проверкой без диалога.
#
# Обход результата ask_many вложенные вызовы переживает: список for
# разворачивается один раз до входа в тело.
#
# Все функции возвращают ненулевой код, если ввод недоступен: без TTY
# (cron, ssh без -t, CGI) read сразу получает EOF, и цикл перезапроса
# крутился бы вечно, сжигая процессор. Вызывающий код обязан этот код
# проверять — либо заранее подставлять значение по умолчанию, как это
# делает install_geoipset для запуска из cron.

# -------------------------------------
# Внутреннее: разбор одного пункта списка
#
# Из строки "ключ|подпись[|состояние]" выставляет _ask_key, _ask_label
# и _ask_state. Отдельной функцией, потому что разбор нужен и рендеру,
# и был бы нужен любому будущему обходу списка.
# -------------------------------------
_ask_parse_item() {
    _ask_key=${1%%|*}
    _ask_rest=${1#*|}
    _ask_label=${_ask_rest%%|*}
    _ask_state=""
    [ "$_ask_rest" != "$_ask_label" ] && _ask_state=${_ask_rest#*|}
}

# -------------------------------------
# Внутреннее: вывод списка пунктов
#
# Один рендер на оба режима. Что именно рисуется, определяют глобальные:
#
#   _ask_cursor  0 — курсора нет (ввод по номерам), иначе номер пункта
#                под курсором
#   _ask_multi   1 — перед номером идёт отметка выбора [x] / [ ]
#
# Выставляет _ask_valid (ключи выбираемых пунктов), _ask_default (ключ
# пункта по умолчанию, если объявлен) и _ask_count (сколько выбираемых).
# -------------------------------------
_ask_render() {
    _ask_valid=""
    _ask_default=""
    _ask_confirm=""
    _ask_count=0
    _ask_render_lines=0    # сколько строк напечатано — для якоря перерисовки

    # Неактивные пункты выравниваются под отметку выбора, иначе номера
    # в списке разъезжаются
    _ask_box_pad=""
    [ "$_ask_multi" = "1" ] && _ask_box_pad="    "

    while [ "$#" -gt 0 ]; do
        _ask_parse_item "$1"

        # Пустой ключ: без подписи — разделитель, с подписью — заголовок
        # группы. Заголовок печатается с отступом пункта, но без номера,
        # поэтому визуально не путается с выбираемым вариантом.
        if [ -z "$_ask_key" ]; then
            if [ -n "$_ask_label" ]; then
                printf '\n  %b\n' "$_ask_label"
                _ask_render_lines=$((_ask_render_lines + 2))
            else
                printf '\n'
                _ask_render_lines=$((_ask_render_lines + 1))
            fi
            shift
            continue
        fi

        if [ "$_ask_state" = "dim" ]; then
            printf '     %s%s. %b%s%b\n' "$_ask_box_pad" "$_ask_key" "$italic" "$_ask_label" "$reset"
            _ask_render_lines=$((_ask_render_lines + 1))
            shift
            continue
        fi

        # confirm-пункт осмыслен только в стрелочном мультивыборе: там его
        # активируют, чтобы зафиксировать набор. В числовом режиме Enter и так
        # коммитит, а в одиночном меню его не бывает — пропускаем целиком.
        if [ "$_ask_state" = "confirm" ] && [ "$_ask_multi" != "1" ]; then
            shift
            continue
        fi

        _ask_count=$((_ask_count + 1))
        _ask_valid="$_ask_valid $_ask_key"
        [ "$_ask_state" = "default" ] && _ask_default="$_ask_key"
        [ "$_ask_state" = "confirm" ] && _ask_confirm="$_ask_key"

        # Действие мультивыбора (Подтвердить/Пропустить) — не выбор, а команда:
        # без отметки [x] и без номера. Обычный пункт мультивыбора несёт отметку.
        _ask_action=0
        if [ "$_ask_multi" = "1" ] \
           && { [ "$_ask_state" = "confirm" ] || [ "$_ask_state" = "default" ]; }; then
            _ask_action=1
        fi

        _ask_box=""
        if [ "$_ask_multi" = "1" ] && [ "$_ask_action" = "0" ]; then
            if _ask_is_selected "$_ask_key"; then
                _ask_box="[x] "
            else
                _ask_box="[ ] "
            fi
        fi

        if [ "$_ask_action" = "1" ]; then
            _ask_line="$_ask_box$_ask_label"
        else
            _ask_line="$_ask_box$_ask_key. $_ask_label"
        fi

        # Стрелка означает курсор в интерактивном режиме и пункт по
        # умолчанию при вводе номерами — в один момент времени доступен
        # только один из режимов, поэтому путаницы не возникает
        if [ "$_ask_cursor" -gt 0 ] 2>/dev/null && [ "$_ask_count" -eq "$_ask_cursor" ]; then
            printf '   %b> %b%b\n' "$light_blue" "$_ask_line" "$reset"
        elif [ "$_ask_cursor" -eq 0 ] 2>/dev/null && [ "$_ask_state" = "default" ]; then
            printf '   > %b\n' "$_ask_line"
        else
            printf '     %b\n' "$_ask_line"
        fi
        _ask_render_lines=$((_ask_render_lines + 1))

        shift
    done
}

# Байт Esc держим в переменной: подстановка команд в паттерне case
# вычислялась бы при каждом нажатии клавиши, то есть fork+exec на каждый
# keypress — заметно при удержании стрелки на слабом процессоре роутера.
_ASK_ESC=$(printf '\033')

# -------------------------------------
# Внутреннее: доступна ли навигация стрелками
#
# Нужен терминал и поддержка read -n/-s/-t. В BusyBox это опции времени
# сборки, поэтому наличие проверяется на месте, а не предполагается:
# на сборке без них навигация недоступна, и меню работает по номерам.
#
# -t проверяется наравне с остальными: без таймаута дочитывание
# escape-последовательности повисает на одиночном Esc.
#
# Результат вычисляется один раз и запоминается в _ask_keys_mode.
# -------------------------------------
_ask_keys_available() {
    if [ -z "$_ask_keys_mode" ]; then
        _ask_keys_mode="no"
        if [ -t 0 ] && [ -t 1 ]; then
            # shellcheck disable=SC3045
            if (printf 'x' | read -r -s -n 1 -t 1 _ask_probe) 2>/dev/null; then
                _ask_keys_mode="yes"
            fi
        fi
    fi

    [ "$_ask_keys_mode" = "yes" ]
}

# -------------------------------------
# Внутреннее: чтение одной клавиши
#
# Возвращает в _ask_key_press: up, down, enter, digit-<цифра>, char-<символ>
# или other (нераспознанное — цикл игнорирует). Смысл символьным клавишам
# присваивает вызывающий цикл: он знает список пунктов, поэтому может отличить
# q-как-выход от q-как-ключ-пункта. Стрелки приходят escape-последовательностью
# из трёх байт, поэтому после ESC дочитываются ещё два — но с таймаутом.
#
# Таймаут обязателен: одиночный Esc это ровно один байт, и безусловное
# чтение продолжения повисло бы на терминале до следующих двух нажатий,
# выглядя как зависание меню. Стрелка присылает все три байта разом, так
# что в обычном случае ожидания не возникает. Дробные значения таймаута
# BusyBox не принимает («invalid timeout»), поэтому одна секунда. Сам
# одиночный Esc НИЧЕГО не делает (выход из меню — только по q): как выход он
# работал заметно медленно из-за этой секунды, поэтому убран.
#
# IFS сбрасывается намеренно: иначе read срезал бы пробел как разделитель,
# и нажатие пробела приходило бы пустой строкой, неотличимой от Enter.
# -------------------------------------
_ask_read_key() {
    _ask_key_press=""

    # shellcheck disable=SC3045
    IFS= read -r -s -n 1 _ask_ch 2>/dev/null || { _ask_key_press="eof"; return 1; }

    # Enter в этом режиме приходит пустой строкой
    if [ -z "$_ask_ch" ]; then
        _ask_key_press="enter"
        return 0
    fi

    case "$_ask_ch" in
        "$_ASK_ESC")
            # Стрелка приходит как ESC [ A/B тремя байтами разом, поэтому [
            # дочитывается сразу. Одиночный Esc — один байт: второй read
            # истекает по таймауту. Третий байт читаем ТОЛЬКО если второй — [,
            # иначе одиночный Esc ждал бы два таймаута вместо одного.
            #
            # Таймаут — целая 1 с (это его пол): дробный этот BusyBox отвергает
            # («invalid timeout»), а -t 0 (неблокирующий опрос) НЕ дочитывает уже
            # готовый байт — стрелку приняли бы за Esc и меню бы вылетало. Читаем
            # продолжение только чтобы распознать стрелку.
            # shellcheck disable=SC3045
            IFS= read -r -s -n 1 -t 1 _ask_ch2 2>/dev/null
            if [ "$_ask_ch2" = "[" ]; then
                # shellcheck disable=SC3045
                IFS= read -r -s -n 1 -t 1 _ask_ch3 2>/dev/null
                case "$_ask_ch3" in
                    A) _ask_key_press="up" ;;
                    B) _ask_key_press="down" ;;
                    *) _ask_key_press="other" ;;
                esac
            else
                # Одиночный Esc игнорируем — выход из меню только по q
                _ask_key_press="other"
            fi
            ;;
        [0-9])  _ask_key_press="digit-$_ask_ch" ;;
        *)      _ask_key_press="char-$_ask_ch" ;;
    esac

    return 0
}

# -------------------------------------
# Внутреннее: проверка, что ключ есть среди активных пунктов
# -------------------------------------
_ask_is_valid() {
    for _ask_v in $_ask_valid; do
        [ "$_ask_v" = "$1" ] && return 0
    done
    return 1
}

# -------------------------------------
# Внутреннее: содержит ли набор ключей пункт по умолчанию рядом с другими.
# «Пропустить» (default) — это отказ, совмещать его с выбором бессмысленно:
# ввод «0 3 4» иначе и пропускал бы, и выполнял действия. Проверяется только
# в числовом мультивыборе — в стрелочном default отдельная команда, не отметка.
# -------------------------------------
_ask_has_default_conflict() {
    [ -n "$_ask_default" ] || return 1
    _ask_n=0
    _ask_has_def=0
    for _ask_c in $1; do
        _ask_n=$((_ask_n + 1))
        [ "$_ask_c" = "$_ask_default" ] && _ask_has_def=1
    done
    [ "$_ask_has_def" = "1" ] && [ "$_ask_n" -gt 1 ]
}

# -------------------------------------
# Внутреннее: сообщение о неверном вводе
# -------------------------------------
_ask_reject() {
    printf '  %bНекорректный ввод.%b %s\n' "$red" "$reset" "${1:-Пожалуйста, выберите снова}"
}

# -------------------------------------
# Внутреннее: ввод недоступен (нет TTY, поток закончился)
#
# Вызывается, когда read вернул EOF. Без этой ветки цикл перезапроса
# крутился бы бесконечно на пустых строках.
# -------------------------------------
_ask_eof() {
    printf '\n  %bВвод недоступен%b: запрос требует интерактивного терминала\n' "$red" "$reset"
    return 1
}

# -------------------------------------
# Выбор одного пункта из списка
#
#   ask_one "Выберите ядро проксирования:" \
#           "1|Xray" \
#           "2|Mihomo" \
#           "|" \
#           "0|Пропустить"
#
# Ключ выбранного пункта остаётся в REPLY_KEY.
# -------------------------------------
ask_one() {
    _ask_title="$1"
    shift

    REPLY_KEY=""

    if _ask_keys_available; then
        _ask_multi=0
        _ask_interactive "$@"
    else
        _ask_multi=0
        _ask_cursor=0
        _ask_one_numeric "$@"
    fi
}

# -------------------------------------
# Внутреннее: выбор вводом номера
#
# Работает всегда: по конвейеру, без терминала и на сборках BusyBox без
# read -n. Остаётся основным путём для неинтерактивных запусков.
# -------------------------------------
_ask_one_numeric() {
    printf '\n'
    [ -n "$_ask_title" ] && printf '  %b\n\n' "$_ask_title"

    _ask_render "$@"

    printf '\n'
    while true; do
        printf '  Ваш выбор: '
        read -r _ask_input || { _ask_eof; return 1; }

        # Пустой ввод засчитывается за пункт по умолчанию, если он объявлен
        [ -z "$_ask_input" ] && [ -n "$_ask_default" ] && _ask_input="$_ask_default"

        if _ask_is_valid "$_ask_input"; then
            REPLY_KEY="$_ask_input"
            return 0
        fi

        _ask_reject "Выберите один из предложенных вариантов"
    done
}

# -------------------------------------
# Внутреннее: выбор стрелками
#
# Один цикл на оба режима, различия задаёт _ask_multi:
#
#   0 — выбор одного пункта: Space/Enter берут пункт под курсором,
#       цифра выбирает сразу
#   1 — выбор нескольких: Space/Enter отмечают переключатель под курсором
#       (или выполняют команду confirm/default), цифра отмечает по номеру,
#       набор фиксируется отдельным пунктом «Подтвердить» (confirm)
#
# Список перерисовывается на месте. Номера продолжают работать —
# привычка нажать цифру сохраняется, и это же оставляет способ выбора
# там, где навигация стрелками неудобна.
#
# Порядок в REPLY_KEYS повторяет порядок пунктов меню, а не порядок
# нажатий — так результат не зависит от того, как пользователь щёлкал.
# -------------------------------------
_ask_interactive() {
    _ask_cursor=1
    _ask_drawn=0
    _ask_selected=""
    _ask_quit_requested=0

    if [ "$_ask_multi" = "1" ]; then
        _ask_hint="↑↓ — выбор, Пробел/Enter — отметить"
    else
        _ask_hint="↑↓ — выбор, Пробел/Enter — выбрать, номер — сразу к пункту"
    fi

    printf '\n'
    [ -n "$_ask_title" ] && printf '  %b\n' "$_ask_title"

    while true; do
        # Перерисовка на месте. Курсор-якорь ставится ПОСЛЕ первой полной
        # отрисовки относительным сдвигом вверх, а не \033[s до неё: длинный
        # вывод прокручивает терминал, и абсолютная \033[s-позиция становится
        # недействительной — меню начинало дописываться лесенкой. К концу
        # первой отрисовки прокрутка уже случилась, поэтому двигаемся вверх на
        # высоту блока, сохраняем якорь и возвращаемся вниз. Ширину знать не
        # нужно, пока пункты не переносятся по строкам (они короткие).
        [ "$_ask_drawn" = "1" ] && printf '\033[u\033[J'

        _ask_render "$@"
        printf '\n  %b%s%b\n' "$italic" "$_ask_hint" "$reset"

        # q выходит только когда есть пункт по умолчанию (слой трактует его как
        # выбор этого пункта) — тогда и подсказываем. Отдельной короткой строкой:
        # длинную подсказку узкий терминал перенёс бы, сбив счёт строк якоря.
        _ask_hint_extra=0
        if [ -n "$_ask_default" ]; then
            printf '  %bq — выход%b\n' "$italic" "$reset"
            _ask_hint_extra=1
        fi

        # Пустой список выбирать не из чего — защита от зависания
        [ "$_ask_count" -eq 0 ] && return 1

        if [ "$_ask_drawn" = "0" ]; then
            _ask_span=$((_ask_render_lines + 2 + _ask_hint_extra))   # +2 подсказка
            printf '\033[%dA\033[s\033[%dB' "$_ask_span" "$_ask_span"
            _ask_drawn=1
        fi

        _ask_read_key || { _ask_eof; return 1; }

        case "$_ask_key_press" in
            up)
                _ask_cursor=$((_ask_cursor - 1))
                [ "$_ask_cursor" -lt 1 ] && _ask_cursor="$_ask_count"
                ;;
            down)
                _ask_cursor=$((_ask_cursor + 1))
                [ "$_ask_cursor" -gt "$_ask_count" ] && _ask_cursor=1
                ;;
            enter|"char- ")
                # Пробел и Enter одинаково активируют пункт под курсором: в
                # одиночном меню — выбирают его, в мультивыборе — отмечают
                # переключатель либо выполняют действие (Подтвердить/Пропустить).
                if _ask_nth_valid "$_ask_cursor"; then
                    _ask_activate "$_ask_nth"
                    [ "$_ask_done" = "1" ] && return 0
                fi
                ;;
            digit-*|char-*)
                _ask_input=${_ask_key_press#*-}

                if _ask_is_valid "$_ask_input"; then
                    _ask_activate "$_ask_input"
                    [ "$_ask_done" = "1" ] && return 0
                elif [ "$_ask_input" = "q" ] || [ "$_ask_input" = "Q" ]; then
                    # q выходит к пункту по умолчанию, но только если он не
                    # занят как ключ пункта: иначе выбрать такой пункт было
                    # бы нельзя, хотя при вводе номерами он доступен
                    _ask_quit_requested=1
                fi
                ;;
        esac

        if [ "$_ask_quit_requested" = "1" ]; then
            _ask_quit_requested=0
            if [ -n "$_ask_default" ]; then
                if [ "$_ask_multi" = "1" ]; then
                    REPLY_KEYS="$_ask_default"
                else
                    REPLY_KEY="$_ask_default"
                fi
                return 0
            fi
        fi
    done
}

# -------------------------------------
# Внутреннее: ключ активного пункта по его порядковому номеру
#
# Результат в _ask_nth. Подстановка команд здесь не годится: она увела бы
# значение в субшелл вместе с чтением терминала.
# -------------------------------------
_ask_nth_valid() {
    _ask_nth=""
    _ask_i=0
    for _ask_v in $_ask_valid; do
        _ask_i=$((_ask_i + 1))
        if [ "$_ask_i" -eq "$1" ]; then
            _ask_nth="$_ask_v"
            return 0
        fi
    done
    return 1
}

# -------------------------------------
# Внутреннее: активировать пункт с ключом $1 по его состоянию.
#
# Единый принцип «Space/Enter активируют пункт под курсором»:
#   - одиночное меню: любой пункт — это выбор, кладём в REPLY_KEY и выходим;
#   - мультивыбор, confirm: фиксируем отмеченный набор в REPLY_KEYS и выходим;
#   - мультивыбор, default: отказ — REPLY_KEYS = ключ default, выходим;
#   - мультивыбор, обычный пункт: переключаем отметку, остаёмся в меню.
#
# Сигнал выхода — _ask_done=1 (вызывающий цикл его проверяет). REPLY_KEYS в
# режиме confirm пересобирается в порядке пунктов меню, а не нажатий.
# -------------------------------------
_ask_activate() {
    _ask_done=0

    if [ "$_ask_multi" != "1" ]; then
        REPLY_KEY="$1"
        _ask_done=1
        return 0
    fi

    if [ -n "$_ask_confirm" ] && [ "$1" = "$_ask_confirm" ]; then
        REPLY_KEYS=""
        for _ask_v in $_ask_valid; do
            _ask_is_selected "$_ask_v" \
                && REPLY_KEYS="${REPLY_KEYS:+$REPLY_KEYS }$_ask_v"
        done
        _ask_done=1
        return 0
    fi

    if [ -n "$_ask_default" ] && [ "$1" = "$_ask_default" ]; then
        REPLY_KEYS="$1"
        _ask_done=1
        return 0
    fi

    _ask_toggle "$1"
    return 0
}


# -------------------------------------
# Выбор нескольких пунктов через пробел
#
# Разделителем принимаются пробел и запятая. Повторы отбрасываются,
# порядок ввода сохраняется. Результат — в REPLY_KEYS.
#
# В стрелочном режиме отметки ставятся по Space/Enter, а набор фиксируется
# командой "confirm" («Подтвердить»); "default" («Пропустить») выходит без
# выбора. В числовом режиме команда confirm не показывается (Enter коммитит
# сам), а "default" нельзя совмещать с номерами (ввод отклоняется).
# -------------------------------------
ask_many() {
    _ask_title="$1"
    shift

    REPLY_KEYS=""

    if _ask_keys_available; then
        _ask_multi=1
        _ask_interactive "$@"
    else
        # Отметок в числовом режиме нет: переключать их нечем, пустые
        # [ ] только вводили бы в заблуждение
        _ask_multi=0
        _ask_cursor=0
        _ask_many_numeric "$@"
    fi
}

# -------------------------------------
# Внутреннее: отмечен ли пункт
# -------------------------------------
_ask_is_selected() {
    case " $_ask_selected " in
        *" $1 "*) return 0 ;;
    esac
    return 1
}

# -------------------------------------
# Внутреннее: переключить отметку пункта
# -------------------------------------
_ask_toggle() {
    if _ask_is_selected "$1"; then
        _ask_rebuilt=""
        for _ask_s in $_ask_selected; do
            [ "$_ask_s" = "$1" ] || _ask_rebuilt="$_ask_rebuilt $_ask_s"
        done
        _ask_selected=${_ask_rebuilt# }
    else
        _ask_selected="${_ask_selected:+$_ask_selected }$1"
    fi
}


# -------------------------------------
# Внутреннее: выбор нескольких пунктов вводом номеров
# -------------------------------------
_ask_many_numeric() {
    printf '\n'
    [ -n "$_ask_title" ] && printf '  %b\n\n' "$_ask_title"

    _ask_render "$@"

    printf '\n'
    while true; do
        printf '  Ваш выбор: '
        read -r _ask_input || { _ask_eof; return 1; }

        # Запятые приравниваем к пробелам: пользователи пишут и так, и так
        _ask_input=$(printf '%s' "$_ask_input" | tr ',' ' ')

        if [ -z "$_ask_input" ]; then
            _ask_reject "Выберите хотя бы один вариант"
            continue
        fi

        _ask_picked=""
        _ask_bad=""

        for _ask_item in $_ask_input; do
            if ! _ask_is_valid "$_ask_item"; then
                _ask_bad="$_ask_item"
                break
            fi

            # Отбрасываем повторный ввод одного и того же номера
            _ask_seen=false
            for _ask_p in $_ask_picked; do
                [ "$_ask_p" = "$_ask_item" ] && _ask_seen=true && break
            done
            [ "$_ask_seen" = false ] && _ask_picked="$_ask_picked $_ask_item"
        done

        if [ -n "$_ask_bad" ]; then
            _ask_reject "Вариант \"$_ask_bad\" недоступен"
            continue
        fi

        # Убираем ведущий пробел, накопленный при склейке
        _ask_picked=${_ask_picked# }

        # Пункт «Пропустить» (default) нельзя совмещать с другими: ввод «3 4 0»
        # иначе и выполнял бы действия, и «пропускал».
        if _ask_has_default_conflict "$_ask_picked"; then
            _ask_reject "Пункт \"$_ask_default\" нельзя выбирать вместе с другими"
            continue
        fi

        REPLY_KEYS="$_ask_picked"
        return 0
    done
}

# -------------------------------------
# Ввод произвольного значения с проверкой
#
#   ask_value "Выберите час (0-23)" _check_hour "Некорректный час"
#
# Второй аргумент — имя функции-валидатора: получает введённое значение
# первым аргументом и возвращает 0, если оно годится. Если валидатор не
# указан, принимается любая непустая строка.
#
# Результат — в REPLY_VALUE.
# -------------------------------------
ask_value() {
    _ask_prompt="$1"
    _ask_check="$2"
    _ask_err="${3:-Пожалуйста, попробуйте снова}"

    REPLY_VALUE=""

    while true; do
        printf '  %s: ' "$_ask_prompt"
        read -r _ask_input || { _ask_eof; return 1; }

        if [ -z "$_ask_input" ]; then
            printf '  %bОшибка%b: Значение не может быть пустым\n' "$red" "$reset"
            continue
        fi

        if [ -z "$_ask_check" ] || "$_ask_check" "$_ask_input"; then
            REPLY_VALUE="$_ask_input"
            return 0
        fi

        printf '  %b%s%b\n' "$red" "$_ask_err" "$reset"
    done
}

# -------------------------------------
# Подтверждение: Да / Нет
#
# Возвращает 0 для «Да» и 1 для «Нет», поэтому подставляется прямо в if.
# Необязательные второй и третий аргументы переопределяют подписи.
#
# Возвращает 2, когда выбор сделать не удалось: нет терминала либо в
# списке не осталось активных пунктов. Для конструкции if это по-прежнему
# «Нет», то есть безопасный отказ от действия, но вызывающий код при
# необходимости может отличить такой случай от осознанного отказа.
# -------------------------------------
ask_yesno() {
    ask_one "$1" "1|${2:-Да}" "0|${3:-Нет}" || return 2
    [ "$REPLY_KEY" = "1" ]
}

# -------------------------------------
# Готовые валидаторы для ask_value
# -------------------------------------

# Целое число в диапазоне: _ask_range_check <значение> <мин> <макс>
_ask_range_check() {
    case "$1" in
        ''|*[!0-9]*) return 1 ;;
    esac
    [ "$1" -ge "$2" ] && [ "$1" -le "$3" ]
}

ask_check_hour() { _ask_range_check "$1" 0 23; }
ask_check_minute() { _ask_range_check "$1" 0 59; }
